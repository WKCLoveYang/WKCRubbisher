#define WKCBundle [NSSearchPathForDirectoriesInDomains(NSDesktopDirectory, NSUserDomainMask, YES).lastObject stringByAppendingPathComponent:@"WKCResource.bundle"]

@implementation WKCResouceBorner

- (instancetype)init
{
    if (self = [super init])
    {
        _resourceCount = 100;
    }
    return self;
}

- (void)startBorn
{
    NSAssert(_thisScriptFullPath, @" script can not be nil !!!");
    
    NSFileManager * fileManamger = [NSFileManager defaultManager];
    BOOL isSuccess = [fileManamger createDirectoryAtPath:WKCBundle
                             withIntermediateDirectories:YES
                                              attributes:nil
                                                   error:nil];
    if (!isSuccess)
    {
        NSLog(@"create bundle failure!");
        return;
    }
    NSLog(@"bundle is create...............");
    NSLog(@"start to create images............");
    
    for (NSInteger index = 0; index < _resourceCount; index ++)
    {
        NSString * imageName = WKCResourceArray[arc4random() % WKCResourceArray.count];
        [self wkc_writeImageWithName:imageName];
    }
}

- (void)wkc_writeImageWithName:(NSString *)name
{
    NSString * imageFullPath = [[_thisScriptFullPath stringByAppendingPathComponent:@"pictures"] stringByAppendingPathComponent:name];
    NSBitmapImageRep * imageRep = (NSBitmapImageRep *)[NSBitmapImageRep imageRepWithContentsOfFile:imageFullPath];
    NSData * imageData = [imageRep representationUsingType:NSBitmapImageFileTypePNG properties:@{}];
    
    NSString * writeFullPath = [WKCBundle stringByAppendingPathComponent:name];
    
    BOOL isWriteSuccess = [imageData writeToFile:writeFullPath
                                      atomically:YES];
    NSLog(@"image %@ write %@ !!!", name, isWriteSuccess ? @"success" : @"failure");
}

